package data;

/**
 * interface Item.
 */
public interface Item {

	/**
	 * getter id.
	 *
	 * @return id of the item
	 */
	public Integer getId();

	/**
	 * setter id.
	 *
	 * @param id the new id
	 */
	public void setId(Integer id);

	/**
	 * getter name.
	 *
	 * @return name of the item
	 */
	public String getName();

	/**
	 * setter name.
	 *
	 * @param name the new name
	 */
	public void setName(String name);

}
